# zip() 函数用于将可迭代的对象作为参数，将对象中对应的元素打包成一个个元组，
# 然后返回由这些元组组成的列表，生成一个二维矩阵。

# 如果各个迭代器的元素个数不一致，则返回列表长度与最短的对象相同，
# 利用 * 号操作符，可以将元组解压为列表。
#

# zip实例
l1 = [1, 2, 3]
l2 = [11, 22, 33]
l3 = ['a', 'b', 'c', 'd']
l4 = zip(l1, l2)
print(type(l4))
print(l4)
# l4 = [(1, 11), (2, 22), (3, 33)]
for i in l4:
    print(i)

# 长度不一致，以短的为准
l5 = zip(l1, l3)
# l5 = [(1, 'a'), (2, 'b'), (3, 'c')]

l6 = zip(l1, l2, l3)
# l6 = [(1, 11, 'a'), (2, 22, 'b'), (3, 33, 'c')]
for i in l6:
    print(i)